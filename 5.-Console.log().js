//console.log() es una función de JavaScript que se utiliza para imprimir mensajes en la consola del navegador o del editor de código.

//Sintaxis

console.log("hola, Paula")

//Podemos averiguar el valor de una varible

const nombre = 'Paula'
console.log(nombre)

//Podemos concatenar cadenas de texto

console.log('Hola '+ nombre)

//Más métodos de console

console.error() // imprime un mensaje de error en la consola
console.warn() // imprime un mensaje de advertencia en la consola
console.info() // imprime un mensaje de información en la consola

console.error('Error')
// ❌ Error
console.warn('Advertencia')
// ⚠️ Advertencia
console.info('Información')
// ℹ️ Información